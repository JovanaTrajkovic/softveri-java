    private void srediFormu() {
        cmbIzlet.getModel().setSelectedItem(g.getIzlet());
        cmbIzlet.setEnabled(false);
        popuniVodice();
        cmbVodic.getModel().setSelectedItem(g.getVodic());
        cmbPrevoz.getModel().setSelectedItem(g.getPrevoz());
        txtKAPACITET.setText(String.valueOf(g.getKapacitetGrupe()));
        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
        txtDatumPolaska.setText(sdf.format(g.getDatumPolaska()));
        popuniPutnike();
        tblClanovi.setModel(new TableModelClanovi(g));

        if (!g.getDatumPolaska().after(new Date())) {
            cmbPutnik.setEnabled(false);
            cmbPrevoz.setEnabled(false);
            cmbVodic.setEnabled(false);
            btnDodajClana.setEnabled(false);
            btnObrisiClana.setEnabled(false);
            btnIzmeni.setEnabled(false);
            txtKAPACITET.setEditable(false);
            txtDatumPolaska.setEditable(false);
            JOptionPane.showMessageDialog(this, "Datum polaska grupe je prosao, ne mozete je menjati!");
        }

    }



    private void popuniVodice() {
        try {
            ArrayList<Vodic> vodici= KlijentKontroler.getInstance().getSveVodice();
            
            cmbVodic.removeAllItems();

            for (Vodic v: vodici) {
                cmbVodic.addItem(v);
            }
        } catch (Exception ex) {
            Logger.getLogger(NovaGrupaForma.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void popuniPutnike() {
     
        try {
            ArrayList<Putnik> putnici = KlijentKontroler.getInstance().getSvePutnike();

            cmbPutnik.removeAllItems();

            for (Putnik putnik : putnici) {
                cmbPutnik.addItem(putnik);
            }
        } catch (Exception ex) {
            Logger.getLogger(NovaGrupaForma.class.getName()).log(Level.SEVERE, null, ex);
        }

        
}
    
    
