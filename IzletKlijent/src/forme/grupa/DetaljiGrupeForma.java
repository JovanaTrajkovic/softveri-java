    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed

    try {
        if (txtKAPACITET.getText().isEmpty() || txtDatumPolaska.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Sva polja moraju biti popunjena!");
            return;
        }
        
        
        int kapacitetGrupe = Integer.parseInt(txtKAPACITET.getText());
        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
        Date datumPolaska  = sdf.parse(txtDatumPolaska.getText());
        
        Vodic v= (Vodic) cmbVodic.getSelectedItem();
        String prevoz = (String) cmbPrevoz.getSelectedItem();
        
        TableModelClanovi tm = (TableModelClanovi) tblClanovi.getModel();
        
        g.setKapacitetGrupe(kapacitetGrupe);
        g.setDatumPolaska(datumPolaska);
        g.setVodic(v);
        g.setPrevoz(prevoz);
        g.setClanovi(tm.getLista());
        
        KlijentKontroler.getInstance().izmeniGrupu(g);
        GlavnaForma gf= (GlavnaForma) getParent();
         gf.refresujTabelu();
        JOptionPane.showMessageDialog(this, "Uspesno izmenjena grupa.");
        this.dispose();
    } catch (Exception ex) {
        Logger.getLogger(DetaljiGrupeForma.class.getName()).log(Level.SEVERE, null, ex);
    }
       

    }//GEN-LAST:event_btnIzmeniActionPerformed

